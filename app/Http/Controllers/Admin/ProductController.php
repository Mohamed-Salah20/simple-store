    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
                $request->validate([
            'name'=>"required|string|max:150",
            'price'=>"required|numeric",
            "description"=>"required|string|max:500",
            "image"=>"required|image|mimes:png,jpg,gif,gpeg"
        ]);

        // $path = $request->file('image')->store('products');
        // $image_name=Storage::disk('public')->put('/',$image);

        $path = Storage::disk('public')->putFile('products', $request->file('image'));

        $product = new Product();
        $product->name = $request->name;
        $product->price = $request->price;
        $product->desc = $request->description;
        $product->img = $path;
        $product->save();

        return back()->with('success','product data inserted  successfully !');

    }

